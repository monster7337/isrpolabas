import math


def area(a, b):
    """
    Возвращает площадь прямоугольника.

    Параметры:
    a (int): Длина прямоугольника.
    b (int): Ширина прямоугольника.

    Возвращаемое значение:
    (a * b) * 2: искомая площадь
    """
    return (a * b) * 2


