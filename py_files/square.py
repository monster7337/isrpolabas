import math


def area(a):
    """
    Возвращает площадь квадрата.

    Параметры:
    a (int): Длина стороны квадрата.

    Возвращаемое значение:
    a * a: искомая площадь
    """
    return a * a


def perimeter(a):
    """
    Возвращает периметр квадрата.

    Параметры:
    a (int): Длина стороны квадрата.

    Возвращаемое значение:
    4 * a: искомый периметр
    """
    return 4 * a